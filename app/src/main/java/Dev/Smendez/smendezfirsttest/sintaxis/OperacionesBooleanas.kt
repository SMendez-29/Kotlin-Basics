package Dev.Smendez.smendezfirsttest.sintaxis

fun main (){

    val num1 = 5
    val num2 = 10
    val isEqual = num1 == num2
    println (isEqual)

    val isNotEqual = num1 != num2
    println (isNotEqual)

    // El = es para asignar un valor, el == es para consultar si ambos valores son iguales
    // El != consulta si hay diferencia entre valores

    val isGreater = num1 > num2
    println (isGreater)

    val isLess = num1 < num2
    println (isLess)

}